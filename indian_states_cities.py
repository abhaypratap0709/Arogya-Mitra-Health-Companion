def get_states():
    """Get list of all Indian states"""
    return list(INDIAN_STATES_CITIES.keys())

def get_cities_for_state(state):
    """Get cities for a specific state"""
    return INDIAN_STATES_CITIES.get(state, [])

def get_all_cities():
    """Get all cities from all states"""
    all_cities = []
    for cities in INDIAN_STATES_CITIES.values():
        all_cities.extend(cities)
    return sorted(all_cities)

def search_cities(query):
    """Search cities by query string"""
    query_lower = query.lower()
    matching_cities = []
    for state, cities in INDIAN_STATES_CITIES.items():
        for city in cities:
            if query_lower in city.lower():
                matching_cities.append((city, state))
    return matching_cities
